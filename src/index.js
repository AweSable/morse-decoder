const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    const codeTable = {};
    Object.keys(MORSE_TABLE).map(el => codeTable[('00000000' + el.replaceAll('.', 10).replaceAll('-', 11)).slice(-10)] = MORSE_TABLE[el]);
    codeTable['**********'] = ' ';
    let res = '';
    for (let i = 0; i < expr.length; i += 10){
        res += codeTable[expr.substr(i, 10)];
        }
    return res;
}

module.exports = {
    decode
}